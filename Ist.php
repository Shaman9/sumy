            удостоєні тільки сотня з них. Зображення прапора слід
            шукати в архівах Петербурга. Гімну у міста спочатку не
            було, зате був полковий марш Сумського гусарського
            полку.</li>
 
            <li>І ще існує легенда, що назва міста
            все-таки відбулася від слова« сумувати ». Тільки козаки
