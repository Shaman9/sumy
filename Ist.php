            Сумського гусарського полку, керувався легендою про
            виявлені в районі майбутньої Сумської фортеці козаками
            Г.Кондратьева трьох сумок із золотом, загублених чи то
            кур'єрами Хмельницького, чи то царськими посланцями.
            Але найімовірніше, що своєю назвою місто зобов'язане
            річкам Сума (тепер Сумка) і Сумка (тепер Стрілка), які
            задовго до заснування міста мали таку назву.</li>
 
            <li>Існує думка, що герб Сум був
