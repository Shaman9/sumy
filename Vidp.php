          більш вчений</p><br>
 
          <h3><p id="h3"><a name="map3" id="map3">Парк
              Асмолова</a></p></h3><img src="img/park32.jpg" align="left"
          hspace="20" height="200" width="300">
 
          <p>Виник парк І. Асмолова в кінці 19 –
          початку 20 ст. на ділянці яку придбав для будівництва
          своєї резиденції Іван Асмолов - підприємць та керуючий у
          цукрозаводчика Харитоненка.</p>
 
          <p>Біля вибудуваного за останньою
          архітектурній моді будинку на 5 гектарах землі був
          розбитий парк з штучними водоймами, де плавали білі і
          чорні лебеді, а також екзотичні риби. Жагучому
          селекціонерові і мандрівникові І. Асмолову зі всієї
          земної кулі привозили екзотичні рослини, які дуже добре
          уживалися з місцевими. У парку було багато клумб і
          пішохідних доріжок. Під час Жовтневої революції парк був
          частково вирубаний, пішохідні доріжки, водойми, галявини,
          клумби та альпійські гірки прийшли в запустіння. Трохи
          пізніше в особняку розміщувався притулок для сиріт, а
          потім санаторій. У роки війни будинок використовували як
          німецький госпіталь. У 1943 р новим господарем парку
          стало обласне лікувально-санаторне управління.</p><br>
          <img src="img/park3.jpg" align="left" hspace="20" height=
          "200" width="300">
 
          <p>У повоєнний час парк почав
          відроджуватися, з'явилися нові види чагарників і дерев.
          На початку 70-х керівництво ЛСУ вирішило оновити парк. До
          реконструкції в парку налічувалося близько 80 видів
          чагарників, дерев і ліан. На сьогоднішній день за
          старовинною садибою І. Асмолова ховаються сріблясті і
          блакитні ялини. Всього в парку росте близько 100
          рідкісних порід листяних і хвойних дерев і близько 30
          сортів бузку.</p>
 
