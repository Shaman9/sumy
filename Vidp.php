            <h3><p id="h3"><a name="map2" id="map2">Парк Казка</a></p></h3><img src=
          "img/park2.jpg" align="left" hspace="20" height="200">
 
          <p>Тим, кому за 30, пам'ятають, як
          захоплююче цікаво було обстежити кам'яні фортеці,
          представляючи себе мешканцями середньовічних замків, або
          влаштовувати справжні піратські баталії на борту
          казкового корабля.</p>
 
          <p>Лялькові вистави в дерев'яної фортеці
          зі співаючим півнем, кафе-морозиво, зовсім ручні лебеді
          та качки, яких можна годувати прямо з руки. Кіт у
          чоботях, поважно знімає капелюха перед дорогими гостями.
          Тепер все це в минулому. А як добре все починалося.</p>
 
          <p>Парк був чарівним, а саме дерев'яна
          фортеця, яку для більшої казковості прикрасили майстерно
          зробленим Фрунзенця півнем, який за сигналом кукурікав.
          Знайшовся кіт, який раніше був прикрасою новорічного
          свята на тому місці, де зараз знаходиться Макдональдс.
          Його відремонтували, одягнули в оксамитовий плащ і
          поставили біля входу кланятися гостям. В одній з кам'яних
          фортець обладнали кінозал, куди цікавих дітлахів впускали
          строгі лицарі в блискучих металевих обладунках. Трохи
          пізніше з'явилися гойдалки. 8 травня 1985, напередодні
          40-річчя Перемоги, відбулося відкриття парку. Так
          острівець казки став улюбленим місцем гуляння дітей
          різного віку і туристів, що приїжджають помилуватися
          дивом з чудес.</p><br>
          <img src="img/park22.jpg" align="left" hspace="20"
          height="200">
 
          <p>За довгі роки роботи парк вимагав
          ремонту. Реанімація була зроблена в 2003 р У парку
          "Казка" з'явилися нові скульптури, старі заново
          пофарбували. Кіт знову стояв на своєму п'єдесталі і
          кланявся.</p>
 
          <p>У свідоцтві про народження, виданому ВАТ
          "СМНВО ім. Фрунзе" дитячому парку "Казка", кланяються
          Кіт, виготовлений у цехах об'єднання, записаний як
          Пантелеймон Муркотик Фрунзенський.</p>
 
          <p>Зріст Кота - 3,5 метра, і він одягнений
          нині в костюм мушкетера. Каркас виготовлений СНВО ім
          Фрунзе, голову, капелюх і хвіст зробили художники театру
